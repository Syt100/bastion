fn render_config_reference(
    cmd: &clap::Command,
    locale: i18n::cli::CliLocale,
) -> Result<String, anyhow::Error> {
    let mut rows = BTreeMap::<String, ConfigEnvRow>::new();
    collect_config_env_rows(cmd, &[command_label(cmd)], &mut rows);

    let mut out = String::new();
    out.push_str(
        "<!-- This file is @generated by `cargo run -p bastion --bin docgen -- --write` -->\n",
    );
    out.push_str("<!-- Do not edit manually. -->\n\n");

    match locale {
        i18n::cli::CliLocale::EnUs => {
            out.push_str("## Resolution precedence\n\n");
            out.push_str(
                "For Hub runtime settings, effective values resolve as: CLI flags → environment variables → saved runtime config (DB, for editable policy fields only) → defaults. `RUST_LOG` is considered after `--log` / `BASTION_LOG` and before DB/default fallbacks.\n\n",
            );
            out.push_str("## CLI-backed configuration environment variables\n\n");
            out.push_str(
                "Root `bastion` flags apply to subcommands unless a subcommand defines its own override.\n\n",
            );
            out.push_str("| Environment | Flags | Scope | Default | Description |\n");
            out.push_str("| --- | --- | --- | --- | --- |\n");
        }
        i18n::cli::CliLocale::ZhCn => {
            out.push_str("## 生效优先级\n\n");
            out.push_str(
                "对于 Hub 运行时配置，最终生效值的优先级为：CLI 参数 → 环境变量 → 已保存的运行配置（数据库，仅限可编辑策略字段）→ 默认值。`RUST_LOG` 会在 `--log` / `BASTION_LOG` 之后、数据库/默认值之前参与回退。\n\n",
            );
            out.push_str("## 由 CLI 定义驱动的配置环境变量\n\n");
            out.push_str(
                "根命令 `bastion` 上的参数默认也适用于其子命令，除非子命令定义了自己的覆盖项。\n\n",
            );
            out.push_str("| 环境变量 | 参数 | 作用域 | 默认值 | 说明 |\n");
            out.push_str("| --- | --- | --- | --- | --- |\n");
        }
    }

    for (env_name, row) in rows {
        out.push_str(&format!(
            "| `{}` | {} | {} | {} | {} |\n",
            env_name,
            markdown_cell_join(&row.flags),
            markdown_cell_join(&row.scopes),
            markdown_cell_join(&row.defaults),
            escape_table_cell(&row.description),
        ));
    }

    match locale {
        i18n::cli::CliLocale::EnUs => {
            out.push_str("\n## Additional environment-only settings\n\n");
            out.push_str("| Environment | Related flags | Scope | Default | Description |\n");
            out.push_str("| --- | --- | --- | --- | --- |\n");
        }
        i18n::cli::CliLocale::ZhCn => {
            out.push_str("\n## 额外的仅环境变量设置\n\n");
            out.push_str("| 环境变量 | 关联参数 | 作用域 | 默认值 | 说明 |\n");
            out.push_str("| --- | --- | --- | --- | --- |\n");
        }
    }

    for row in EXTRA_ENV_ROWS {
        let description = match locale {
            i18n::cli::CliLocale::EnUs => row.description_en,
            i18n::cli::CliLocale::ZhCn => row.description_zh,
        };
        out.push_str(&format!(
            "| `{}` | {} | {} | {} | {} |\n",
            row.name,
            escape_table_cell(row.flags),
            escape_table_cell(row.scope),
            escape_table_cell(row.default_value),
            escape_table_cell(description),
        ));
    }

    Ok(out)
}

fn collect_config_env_rows(
    cmd: &clap::Command,
    path: &[String],
    rows: &mut BTreeMap<String, ConfigEnvRow>,
) {
    for arg in cmd.get_arguments() {
        let Some(env_name) = arg.get_env() else {
            continue;
        };

        let env_name = env_name.to_string_lossy().into_owned();
        let row = rows.entry(env_name).or_default();
        row.scopes.insert(path.join(" "));

        if let Some(long) = arg.get_long() {
            row.flags.insert(format!("`--{long}`"));
        }
        if let Some(short) = arg.get_short() {
            row.flags.insert(format!("`-{short}`"));
        }

        for default in arg.get_default_values() {
            let value = default.to_string_lossy().trim().to_string();
            if !value.is_empty() {
                row.defaults
                    .insert(format!("`{}`", escape_backticks(&value)));
            }
        }

        if row.description.is_empty() {
            row.description = arg
                .get_long_help()
                .or_else(|| arg.get_help())
                .map(|value| collapse_whitespace(&value.to_string()))
                .unwrap_or_else(|| "—".to_string());
        }
    }

    for sub in cmd.get_subcommands() {
        if sub.is_hide_set() {
            continue;
        }
        let mut next = path.to_vec();
        next.push(sub.get_name().to_string());
        collect_config_env_rows(sub, &next, rows);
    }
}

fn command_label(cmd: &clap::Command) -> String {
    cmd.get_bin_name()
        .map(|value| value.to_string())
        .unwrap_or_else(|| cmd.get_name().to_string())
}

fn collapse_whitespace(input: &str) -> String {
    input.split_whitespace().collect::<Vec<_>>().join(" ")
}

fn markdown_cell_join(values: &BTreeSet<String>) -> String {
    if values.is_empty() {
        return "—".to_string();
    }
    escape_table_cell(&values.iter().cloned().collect::<Vec<_>>().join(", "))
}

fn escape_table_cell(value: &str) -> String {
    value.replace('|', "\\|")
}

fn escape_backticks(value: &str) -> String {
    value.replace('`', "\\`")
}

fn set_term_width_recursive(cmd: clap::Command, width: usize) -> clap::Command {
    cmd.term_width(width)
        .mut_subcommands(|sub| set_term_width_recursive(sub, width))
}

fn write_file(path: &Path, contents: &str) -> Result<(), anyhow::Error> {
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent)
            .with_context(|| format!("create dir {}", parent.display()))?;
    }
    std::fs::write(path, contents).with_context(|| format!("write {}", path.display()))
}

fn check_file(path: &Path, contents: &str) -> Result<(), anyhow::Error> {
    let current = std::fs::read_to_string(path)
        .with_context(|| format!("read {}", path.display()))
        .unwrap_or_default();
    if current != contents {
        anyhow::bail!(
            "generated docs out of date: {}\n\
run: cargo run -p bastion --bin docgen -- --write",
            path.display()
        );
    }
    Ok(())
}

#[cfg(test)]
mod tests {
    use super::{build_localized_command, render_cli_reference, render_config_reference};
    use crate::i18n::cli::CliLocale;

    #[test]
    fn cli_reference_includes_new_commands() {
        let command = build_localized_command(CliLocale::EnUs);
        let markdown = render_cli_reference(&command.command).expect("render");
        assert!(markdown.contains("## bastion config"));
        assert!(markdown.contains("## bastion doctor"));
    }

    #[test]
    fn config_reference_includes_cli_and_env_only_rows() {
        let command = build_localized_command(CliLocale::EnUs);
        let markdown = render_config_reference(&command.command, CliLocale::EnUs).expect("render");
        assert!(markdown.contains("`BASTION_HOST`"));
        assert!(markdown.contains("`BASTION_DATA_DIR`"));
        assert!(markdown.contains("`BASTION_UI_DIR`"));
        assert!(markdown.contains("`RUST_LOG`"));
    }
}
